using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Data.Entity;

namespace Hotel.Database.Staff
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A staff context. </summary>
    ///
    /// <remarks>   Student, 19.12.2016. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class StaffContext : DbContext
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the directors. </summary>
        ///
        /// <value> The directors. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DbSet<Director> Directors { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the jobs. </summary>
        ///
        /// <value> The jobs. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DbSet<Job> Jobs { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the supervisors. </summary>
        ///
        /// <value> The supervisors. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DbSet<Supervisor> Supervisors { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the subordinates. </summary>
        ///
        /// <value> The subordinates. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DbSet<Subordinate> Subordinates { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the employee statuses. </summary>
        ///
        /// <value> The employee statuses. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DbSet<EmployeeStatus> EmployeeStatuses { get; set; }
    }
}

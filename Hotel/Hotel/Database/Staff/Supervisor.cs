using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.ComponentModel.DataAnnotations.Schema;
using System.ComponentModel.DataAnnotations;

namespace Hotel.Database.Staff
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A supervisor. </summary>
    ///
    /// <remarks>   Student, 19.12.2016. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class Supervisor : Employee
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [Key]
        public int id { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name. </summary>
        ///
        /// <value> The name. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string name { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the lastname. </summary>
        ///
        /// <value> The lastname. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string lastname { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the subordinates. </summary>
        ///
        /// <value> The subordinates. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public virtual List<Employee> subordinates { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the wage. </summary>
        ///
        /// <value> The wage. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public float wage { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the employee status. </summary>
        ///
        /// <value> The employee status. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public virtual EmployeeStatus employeeStatus { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the jobs. </summary>
        ///
        /// <value> The jobs. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public virtual List<Job> jobs { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Student, 19.12.2016. </remarks>
        ///
        /// <param name="id">           The identifier. </param>
        /// <param name="name">         The name. </param>
        /// <param name="lastname">     The lastname. </param>
        /// <param name="subordinates"> The subordinates. </param>
        /// <param name="wage">         The wage. </param>
        /// <param name="status">       The status. </param>
        /// <param name="jobs">         The jobs. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Supervisor(int id, string name, string lastname, List<Employee> subordinates, float wage, EmployeeStatus status, List<Job> jobs)
        {
            this.id = id;
            this.name = name;
            this.lastname = lastname;
            this.subordinates = subordinates;
            this.wage = wage;
            this.employeeStatus = status;
            this.jobs = jobs;
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Returns a string that represents the current object. </summary>
        ///
        /// <remarks>   Student, 19.12.2016. </remarks>
        ///
        /// <returns>   A string that represents the current object. </returns>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string ToString()
        {
            return name + " " + lastname;
        }
    }
}

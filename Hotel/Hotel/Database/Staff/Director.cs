using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.ComponentModel.DataAnnotations.Schema;
using System.ComponentModel.DataAnnotations;

namespace Hotel.Database.Staff
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A director. </summary>
    ///
    /// <remarks>   Student, 19.12.2016. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class Director
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [Key]
        public int id { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name. </summary>
        ///
        /// <value> The name. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string name { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the lastname. </summary>
        ///
        /// <value> The lastname. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string lastname { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the supervisors. </summary>
        ///
        /// <value> The supervisors. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public virtual List<Employee> supervisors { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Student, 19.12.2016. </remarks>
        ///
        /// <param name="id">           The identifier. </param>
        /// <param name="name">         The name. </param>
        /// <param name="lastname">     The lastname. </param>
        /// <param name="supervisors">  The supervisors. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Director(int id, string name, string lastname, List<Employee> supervisors)
        {
            this.id = id;
            this.name = name;
            this.lastname = lastname;
            this.supervisors = supervisors;
        }
    }
}

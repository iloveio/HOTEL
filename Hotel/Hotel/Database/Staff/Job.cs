using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.ComponentModel.DataAnnotations.Schema;
using System.ComponentModel.DataAnnotations;

namespace Hotel.Database.Staff
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A job. </summary>
    ///
    /// <remarks>   Student, 19.12.2016. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class Job
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the job supervisor. </summary>
        ///
        /// <value> The job supervisor. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public virtual Supervisor jobSupervisor { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets information describing the job. </summary>
        ///
        /// <value> Information describing the job. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [Key]
        public string jobDescription { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the deadline. </summary>
        ///
        /// <value> The deadline. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime deadline { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Student, 19.12.2016. </remarks>
        ///
        /// <param name="supervisor">   The supervisor. </param>
        /// <param name="desc">         The description. </param>
        /// <param name="deadline">     The deadline Date/Time. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Job(Supervisor supervisor, string desc, DateTime deadline)
        {
            this.jobSupervisor = supervisor;
            this.jobDescription = desc;
            this.deadline = deadline;
        }
    }
}
